using System;
using System.Collections.Generic;
using System.Text;

namespace Polygons.Helper
{
    class Converter
    {
        public static int doubleToInteger(double value)
        {
            return Convert.ToInt32(value);
        }

        public static int stringToInteger(String value)
        {
            return Convert.ToInt32(value);
        }
    }
}
